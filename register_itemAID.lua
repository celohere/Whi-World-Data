byAID = {} -- stops trading and moving these objects
local function generateByAID(t)
    for key, AID in pairs(t) do
        if type(AID) == "table" then
            generateByAID(AID)
        elseif type(AID) == "string" then
            local AID = tonumber(AID)
            t[key] = AID
            byAID[AID] = true
        end
    end
end
